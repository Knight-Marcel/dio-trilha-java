public class Operacoes {
     


    public static void Somar(double numero1 , double numero2){
        double resultado = numero1 + numero2;
        System.out.println("A soma do "+numero1+" mais "+numero2+" é :"+resultado );
    }

    public static void Dividir(double numero1 , double numero2){
        double resultado = numero1 / numero2;
        System.out.println("A divisão do "+numero1+" mais "+numero2+" é :"+resultado);
    }
    public static void Multiplicacao(double numero1 , double numero2){
        double resultado = numero1 * numero2;
        System.out.println("A multiplicação do "+numero1+" mais "+numero2+"é : "+resultado);
    }

    public static void Subtracao(double numero1 , double numero2){
        double resultado = numero1 - numero2;
        System.out.println("A Subtração do "+numero1+" mais "+numero2+"é : "+resultado);
    }


    }

